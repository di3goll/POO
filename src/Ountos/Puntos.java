package Ountos;

/**
 *
 * @author di3go
 */
public class Puntos {

    int x, y;

    public Puntos(int x, int y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "(" + x + "," + y + ")";
    }
}
